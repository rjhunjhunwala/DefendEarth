package GravityAPI;

import java.util.ArrayList;

/**
 * A flight physics simulator that can simulate the flight path of an object
 * with an initial vector velocity. The only forces acting upon the object are
 * the launching forces and gravity. Acceleration is instant. The only other
 * force acting upon the object is gravity. The force of gravity can be changed
 * but by default, is set to 9.8 meters/(second^2).
 *
 * @author Nick Villano
 */
public class Demo {

    /**
     * Acceleration due to gravity in meters/(second^2). The default value is
     * 9.8 meters/(second^2).
     */
    private static double GRAVITY = 9.8; // meters/(second^2)

    /**
     * Sets the force of gravity to a value determined by the parameters.
     *
     * @param inG The new force of gravity in meters/(second^2).
     */
    public static void setGravity(double inG) {
        GRAVITY = inG;
    }

    /**
     * Resets the force of gravity to its default value of 9.8
     * meters/(second^2).
     */
    public static void gravityToDefault() {
        GRAVITY = 9.8;
    }

    /**
     * An example of this class in use.
     */
    public static void demo() {

        AccuratePoint startPos = new AccuratePoint(0, 0);
        double initialAngle = 45;
        double initialVelocity = 15;
        int simulationAccuracy = 5;

        printFlightPath(startPos, initialAngle, initialVelocity, simulationAccuracy, -100.0, 0, 100.0, 100.0);
    }

    /**
     * Calculates the path on a coordinate plane of the flight path of a generic
     * object represented by a AccuratePoint. The path is based off of given
     * parameters. The simulation ends as soon as the projectile leaves the
     * boundaries that are defined by the parameters.
     *
     * @param shooterPosition The position of the shooter.
     * @param launchAngle The initialAngle from parallel with the ground of the
     * launched object.
     * @param initialVelocity The initial velocity of the object being launched.
     * This method assumes instant acceleration.
     * @param accuracy The number of calculations per second in the simulation
     * that the program should perform.
     * @param minX The left bound for the simulation.
     * @param minY The bottom bound for the simulation.
     * @param maxX The right bound for the simulation.
     * @param maxY The top bound for the simulation.
     * @return An array of AccuratePoints (like a Point but with doubles instead
     * of integers) that represents the different points of the flight path.
     */
    public static AccuratePoint[] flightPath(AccuratePoint shooterPosition, double launchAngle, double initialVelocity, int accuracy,
            double minX, double minY, double maxX, double maxY) {
        ArrayList<AccuratePoint> path = new ArrayList<>();

        double vix = xVelocity(initialVelocity, launchAngle); // initial x velocity
        double viy = yVelocity(initialVelocity, launchAngle); // initial y velocity

        double tick = 0.0;
        do {
            path.add(new AccuratePoint((vix * tick + shooterPosition.x), ((viy - GRAVITY * tick) * tick + shooterPosition.y)));
            tick += 1 / (double) accuracy;
        } while ((viy - GRAVITY * tick) * tick + shooterPosition.y >= minY
                && (viy - GRAVITY * tick) * tick + shooterPosition.y <= maxY
                && vix * tick + shooterPosition.x >= minX
                && vix * tick + shooterPosition.x <= maxX);

        AccuratePoint[] toReturn = new AccuratePoint[path.size()];
        for (int i = 0; i < toReturn.length; i++) {
            toReturn[i] = path.get(i);
        }

        return toReturn;
    }

    /**
     * Calculates and prints to the console the path on a coordinate plane of
     * the flight path of a generic object represented by a AccuratePoint. The
     * path is based off of given parameters. The path ends when the object is
     * calculated to go below -5 on the horizontal axis. The starting
     * coordinates should be above -5 on the horizontal axis or else only the
     * starting point will be graphed.
     *
     * The format is as follows: the x coordinate followed by a tab followed by
     * the y coordinate followed by a line break followed by the next x
     * coordinate and so on. This format allows for the text printed to the
     * console to be simply copied and pasted into an Excel spread sheet and
     * graphed.
     *
     * @param shooterPosition The position of the shooter.
     * @param launchAngle The initialAngle from parallel with the ground of the
     * launched object.
     * @param initialVelocity The initial velocity of the object being launched.
     * This method assumes instant acceleration.
     * @param accuracy The number of calculations per second in the simulation
     * that the program should perform.
     * @param minX The left bound for the simulation.
     * @param minY The bottom bound for the simulation.
     * @param maxX The right bound for the simulation.
     * @param maxY The top bound for the simulation.
     */
    public static void printFlightPath(AccuratePoint shooterPosition, double launchAngle, double initialVelocity, int accuracy,
            double minX, double minY, double maxX, double maxY) {
        AccuratePoint[] path = flightPath(shooterPosition, launchAngle, initialVelocity, accuracy, minX, minY, maxX, maxY);
        for (AccuratePoint p : path) {
            System.out.println(p.x + "\t" + p.y);
        }
    }

    /**
     * Calculates the horizontal velocity based off of the velocity of a vector
     * and the initialAngle of the vector in relation to the ground.
     *
     * @param v1 The initial velocity of the vector.
     * @param launchAngle The launch initialAngle of the vector in relation to
     * the ground.
     * @return The horizontal velocity.
     */
    private static double xVelocity(double v1, double launchAngle) {
        return Math.cos(Math.toRadians(launchAngle)) * v1;
    }

    /**
     * Calculates the vertical velocity based off of the velocity of a vector
     * and the initialAngle of the vector in relation to the ground.
     *
     * @param v1 The initial velocity of the vector.
     * @param launchAngle The launch initialAngle of the vector.
     * @return The vertical velocity.
     */
    private static double yVelocity(double v1, double launchAngle) {
        return Math.sin(Math.toRadians(launchAngle)) * v1;
    }

    public static AccuratePoint[] jFrameFlightPath(AccuratePoint shooterPosition, double launchAngle, double initialVelocity, int accuracy,
            double minX, double minY, double maxX, double maxY, int jFrameHeight) {
        AccuratePoint[] path = flightPath(shooterPosition, launchAngle, initialVelocity, accuracy,
                minX, minY, maxX, maxY);
        for (int i = 0; i < path.length; i++) {
            path[i].y = jFrameHeight - path[i].y;
        }
        return path;
    }

    public static void printJFrameFlightPath(AccuratePoint shooterPosition, double launchAngle, double initialVelocity, int accuracy,
            double minX, double minY, double maxX, double maxY, int jFrameHeight) {
        AccuratePoint[] path = jFrameFlightPath(shooterPosition, launchAngle, initialVelocity, accuracy, minX, minY, maxX, maxY, jFrameHeight);
        for (AccuratePoint p : path) {
            System.out.println(p.x + "\t" + p.y);
        }
    }

}
